package day25_method;

public class Calculator {

	public void MultiplicationTable(int a, int b) {
		System.out.println(a + " X " + b+ " = "+ (a*b));
		}
	
		
		public void DivisonTable(int a, int b) {
			System.out.println(a + " / " + b+ " = "+ (a/b));
		
		}
	
		public void SubtractionTable(int a, int b) {
			System.out.println(a + " - " + b+ " = "+ (a-b));
		
		}
	
		public void AdditionTable(int a, int b) {
			
			System.out.println(a + " + " + b+ " = "+ (a+b));
	}
}
