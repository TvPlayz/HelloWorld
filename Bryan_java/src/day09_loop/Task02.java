package day09_loop;

public class Task02 {

	public static void main(String[] args) {
		
		int num1 = 1;
        int num2 = 10;
        
 
        
        while (num1 <= num2) {
        	num2--;
            System.out.println(num1 + " * " + num2 + " = " + num1 * num2);
        
        }
        
        
        
	}
}
