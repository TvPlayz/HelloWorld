package day08_2ndreview;

public class ComparisonOperDemo {

	public static void main(String[] args) {
		// Comparison / Relational
		// binary operator , use with 2 values/ data
		
		// ==, !=, >, >=, <, <=
		
		// return boolean 'true' or 'false'
		// use with primitive data most of the time
		
		int x = 1;
		int y = 2;
		boolean isEqual = x == y;
		System.out.println(isEqual); // false
		
		char letter1 = 'a';
		char letter2 = 'a';
		System.out.println(letter1 != letter2); // false
		
		System.out.println(10 >= 11); // false
		System.out.println(5 < 45); // true
		

	}

}
