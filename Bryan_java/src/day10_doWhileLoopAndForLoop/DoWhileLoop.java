package day10_doWhileLoopAndForLoop;

public class DoWhileLoop {

	public static void main(String[] args) {
		
		int i = 0;
		do {
			System.out.println(i);
			i++;
		}
		while (i < 5);

		
		System.out.println("Value of i id " + i);
		
		System.out.println("---------------------------------");
		
		 i = 10;
		do {
			System.out.println(i);
			i--;
		}
		while (i >= -10);
		
		
		
	}

}
