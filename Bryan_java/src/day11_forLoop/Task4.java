package day11_forLoop;

public class Task4 {

	public static void main(String[] args) {
		
		for (int i = 1;i <= 30;i++) {
			if (i >= 20) {
			System.out.println(i);
		}
	}
		
		int num = 20;
		
		do {
			System.out.println(num);
		}while (num <= 30);
		
		
		int summer = 20;
		
		while (summer <= 30) {
			System.out.println(summer);
			summer++;
		}
		
	}

}
